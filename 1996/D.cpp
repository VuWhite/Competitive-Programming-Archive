void run_case() {
    int n, x;
    cin >> n >> x;
    long long tot = 0;
    for (int i = 1; i <= x - 2; i++) {
        for (int j = 1; j <= min(n / i, x - i - 1); j++) {
            int mxC = min((n - i * j) / (i + j), x - i - j);
            if (mxC > 0) tot += mxC;
        }
    }
    cout << tot << '\n';
}
